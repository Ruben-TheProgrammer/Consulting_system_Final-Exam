    private void BotonGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonGuardarMouseClicked
        try{
            String cedula = campoCedula.getText();
            String nombre = campoNombre.getText();
            java.util.Date fecha_nacimiento = campoFecha.getDate();

            String sqlguardar = "INSERT INTO paciente (cedula, nombre, fecha_nacimiento) VALUES (?, ?, ?)";
            PreparedStatement ps = coneccionsql.prepareStatement(sqlguardar);
            ps.setString(1, cedula);
            ps.setString(2, nombre);
            ps.setDate(3, convertUtilToSql(fecha_nacimiento));

            int n = ps.executeUpdate();
            if (n > 0) {
                campoCedula.setText("");
                campoNombre.setText("");

                llenarTablar();
                JOptionPane.showMessageDialog(this, "Se guardó correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar");
            }
            ps.close();
        }catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane, "Error: No se podo registrar al cliente!");
    }//GEN-LAST:event_BotonGuardarMouseClicked
    }
    
    private void botonEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonEditarMouseClicked
        try {
            String cedula = campoCedula.getText();
            String nombre = campoNombre.getText();
            java.util.Date fecha_nacimiento = campoFecha.getDate();

            //Guardar en la base de datos
            String stactualizar = "UPDATE paciente SET cedula=?, nombre=?, fecha_nacimiento=? WHERE id_paciente=?";
            PreparedStatement ps = coneccionsql.prepareStatement(stactualizar);
            ps.setString(1, cedula);
            ps.setString(2, nombre);
            ps.setDate(3, convertUtilToSql(fecha_nacimiento));
            ps.setInt(4, id_paciente);

            int n = ps.executeUpdate();
            if (n > 0) {
                //limpiar formulario
                campoCedula.setText("");
                campoNombre.setText("");

                //reiniciar el estado de los botones
                BotonGuardar.setVisible(false);
                botonEditar.setVisible(true);

                llenarTablar();
                JOptionPane.showMessageDialog(this, "Se actualizo correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }
            ps.close();

        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(rootPane, "Error: No se podo Editar al paciente!");
        }
    }//GEN-LAST:event_botonEditarMouseClicked

    private void botonBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonBorrarMouseClicked
        // TODO add your handling code here:
        try {

            //Borrar registro en la base de datos
            String stborrar = "DELETE FROM paciente WHERE id_paciente =" + id_paciente;
            PreparedStatement ps = coneccionsql.prepareStatement(stborrar);
            int n = ps.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(this, "Se borro correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Error al borrar");
            }
            ps.close();

            llenarTablar();
            //limpiar formulario
            campoCedula.setText("");
            campoNombre.setText("");
            
            //Reiniciar el estado de los botones
            BotonGuardar.setVisible(true);
            botonEditar.setVisible(false);
            botonBorrar.setVisible(false);

        } catch (Exception e) {
            System.err.println(e);
        }        
    }//GEN-LAST:event_botonBorrarMouseClicked

    private void tablaPacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPacienteMouseClicked
        // TODO add your handling code here:
        if (evt.getButton() == 1) {
            int fila = tablaPaciente.getSelectedRow();
            try {
                String stsql = "SELECT id_paciente, cedula, nombre, fecha_nacimiento FROM paciente\n WHERE id_paciente = " + tablaPaciente.getValueAt(fila, 0);
                sentenciasql = coneccionsql.createStatement();
                ResultSet rs = sentenciasql.executeQuery(stsql);
                rs.next();

                id_paciente = rs.getInt("id_paciente");
                campoCedula.setText(rs.getString("cedula"));
                campoNombre.setText(rs.getString("nombre"));
                campoFecha.setDate(rs.getDate("fecha_nacimiento"));

                botonEditar.setVisible(true);
                BotonGuardar.setVisible(false);
                botonBorrar.setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_tablaPacienteMouseClicked


