    public void llenarTablar() {
        try {
            //encabezados de la tabla
            String[] titulos = {"ID", "Cedula", "Nombre", "fecha_nacimiento"};
            String stsql = "SELECT paciente.id_paciente, paciente.cedula, paciente.nombre, paciente.fecha_nacimiento FROM paciente";

            modelo_tabla = new DefaultTableModel(null, titulos);
            sentenciasql = coneccionsql.createStatement();
            ResultSet rs = sentenciasql.executeQuery(stsql);

            //arreglo fila, para almacenar registros
            String[] fila = new String[4];
            //while para insertar registros en la tabla
            while (rs.next()) {
                fila[0] = rs.getString("id_paciente");
                fila[1] = rs.getString("cedula");
                fila[2] = rs.getString("nombre");
                fila[3] = rs.getString("fecha_nacimiento");
                modelo_tabla.addRow(fila);
            }

            //agrego el default model
            tablaPaciente.setModel(modelo_tabla);

            //definir el ancho de las columnas
            int[] anchos = {10, 100, 100, 100};
            for (int i = 0; i < tablaPaciente.getColumnCount(); i++) {
                tablaPaciente.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }    

